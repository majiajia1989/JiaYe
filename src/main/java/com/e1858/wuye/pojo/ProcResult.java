package com.e1858.wuye.pojo;


public class ProcResult
{
	private Boolean procStatus=false;
	private String procText="";
	private String procReserve="";
	public Boolean getProcStatus()
	{
		return procStatus;
	}
	public void setProcStatus(Boolean procStatus)
	{
		this.procStatus = procStatus;
	}
	public String getProcText()
	{
		return procText;
	}
	public void setProcText(String procText)
	{
		this.procText = procText;
	}
	public String getProcReserve()
	{
		return procReserve;
	}
	public void setProcReserve(String procReserve)
	{
		this.procReserve = procReserve;
	}
	
	
	
}
